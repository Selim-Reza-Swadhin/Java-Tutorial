package begennerjava;

public class WhileLoop {

    public static void main(String[] args) {
        int i = 1;
        while (i < 10) {
            System.out.println("Hello = " + i);
            i++;
        }
        
        //even number
        int j = 2;
        while (j < 10) {
            System.out.println("Hello = " + j);
            j= j +2;
        }
        
        //odd number
        int k = 1;
        while (k < 10) {
            System.out.println("Hello = " + k);
            k= k + 2;
        }

    }
}
